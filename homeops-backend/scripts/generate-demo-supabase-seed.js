"use strict";

/**
 * Reads data/demo-properties.json and writes scripts/demo_seed_supabase.sql.
 * Run: node scripts/generate-demo-supabase-seed.js
 */

const fs = require("fs");
const path = require("path");
const bcrypt = require("bcrypt");
const { customAlphabet } = require("nanoid");

const genUid = customAlphabet("23456789ABCDEFGHJKLMNPQRSTUVWXYZ", 8);

const SRC = path.join(__dirname, "..", "data", "demo-properties.json");
const OUT = path.join(__dirname, "demo_seed_supabase.sql");
const WORK_FACTOR = 12;

function esc(s) {
  if (s == null) return "NULL";
  return "'" + String(s).replace(/'/g, "''") + "'";
}

function generatePassportId(state, zip) {
  const rand = String(Math.floor(10000 + Math.random() * 90000));
  return `${state}-${zip}-${rand}`;
}

(async () => {
  const data = JSON.parse(fs.readFileSync(SRC, "utf8"));
  const agent = data.agent;
  const props = data.properties;

  const agentHash = await bcrypt.hash(agent.password, WORK_FACTOR);
  const homeownerHash = await bcrypt.hash("12345678", WORK_FACTOR);

  const lines = [];
  const w = (s) => lines.push(s);

  w(`-- =============================================================================`);
  w(`-- Opsy demo seed for Supabase SQL editor (PostgreSQL)`);
  w(`-- Generated by: node scripts/generate-demo-supabase-seed.js`);
  w(`-- Source: data/demo-properties.json`);
  w(`--`);
  w(`-- Logins (local auth / users table):`);
  w(`--   Agent:     ${agent.email} / ${agent.password}`);
  w(`--   Homeowner: firstname.lastname@email.com / 12345678`);
  w(`--`);
  w(`-- Safe to re-run: uses ON CONFLICT DO NOTHING where possible;`);
  w(`--   re-running after partial failure may skip some rows — prefer a clean DB or delete demo rows.`);
  w(`-- This does NOT use Supabase Auth (auth.users); your API must use the \`users\` table.`);
  w(`-- If row-level security (RLS) is enabled on these tables, run as a role that bypasses RLS`);
  w(`-- (e.g. postgres) or temporarily relax policies for this seed.`);
  w(`--`);
  w(`-- Property photos are full https URLs (Unsplash). The backend presigned-URL helper`);
  w(`-- passes through http(s) URLs, so main_photo_url is populated automatically.`);
  w(`-- =============================================================================`);
  w(``);
  w(`BEGIN;`);

  w(``);
  w(`-- ---- Agent user ----`);
  w(`INSERT INTO users (email, password_hash, name, phone, role, contact_id, is_active, auth_provider, email_verified, avatar_url, subscription_tier)`);
  w(`VALUES (`);
  w(`  ${esc(agent.email)},`);
  w(`  ${esc(agentHash)},`);
  w(`  ${esc(agent.name)},`);
  w(`  ${esc(agent.phone)},`);
  w(`  'agent'::user_role,`);
  w(`  0,`);
  w(`  true,`);
  w(`  'local',`);
  w(`  true,`);
  w(`  ${esc(agent.avatar_url)},`);
  w(`  'agent_premium'`);
  w(`)`);
  w(`ON CONFLICT (email) DO UPDATE SET`);
  w(`  password_hash = EXCLUDED.password_hash,`);
  w(`  name = EXCLUDED.name,`);
  w(`  phone = EXCLUDED.phone,`);
  w(`  role = EXCLUDED.role,`);
  w(`  is_active = EXCLUDED.is_active,`);
  w(`  avatar_url = EXCLUDED.avatar_url,`);
  w(`  email_verified = EXCLUDED.email_verified,`);
  w(`  subscription_tier = EXCLUDED.subscription_tier,`);
  w(`  updated_at = NOW();`);

  w(``);
  w(`-- ---- Agent account + Win (agent_premium) subscription ----`);
  w(`INSERT INTO accounts (name, url, owner_user_id)`);
  w(`SELECT ${esc(agent.name + ' (Demo)')}, 'demo-agent', u.id`);
  w(`FROM users u WHERE u.email = ${esc(agent.email)}`);
  w(`ON CONFLICT (url) DO NOTHING;`);
  w(``);
  w(`INSERT INTO account_users (account_id, user_id, role)`);
  w(`SELECT a.id, u.id, 'owner'::account_role`);
  w(`FROM accounts a JOIN users u ON u.email = ${esc(agent.email)}`);
  w(`WHERE a.url = 'demo-agent'`);
  w(`ON CONFLICT DO NOTHING;`);
  w(``);
  w(`INSERT INTO subscription_products (name, description, target_role, code, price, sort_order, is_active, max_properties, max_contacts, max_viewers, max_team_members)`);
  w(`VALUES ('Win', 'Agent Win plan', 'agent', 'agent_premium', 249, 6, true, 999, 9999, 100, 100)`);
  w(`ON CONFLICT (code) DO NOTHING;`);
  w(``);
  w(`INSERT INTO account_subscriptions (account_id, subscription_product_id, status, current_period_start, current_period_end)`);
  w(`SELECT a.id, sp.id, 'active', NOW(), NOW() + INTERVAL '1 year'`);
  w(`FROM accounts a, subscription_products sp`);
  w(`WHERE a.url = 'demo-agent' AND sp.code = 'agent_premium'`);
  w(`AND NOT EXISTS (SELECT 1 FROM account_subscriptions asub WHERE asub.account_id = a.id);`);

  for (const p of props) {
    const ho = p.homeowner;
    const addr = p.address;
    const pad = String(p.index).padStart(3, "0");
    const slug = `demo-hw-${pad}`;
    const uid = genUid();
    const passportId = generatePassportId(addr.state, addr.zip);

    w(``);
    w(`-- ---- Property ${p.index}: ${addr.full} ----`);

    w(`INSERT INTO users (email, password_hash, name, phone, role, contact_id, is_active, auth_provider, email_verified, avatar_url)`);
    w(`VALUES (`);
    w(`  ${esc(ho.email)},`);
    w(`  ${esc(homeownerHash)},`);
    w(`  ${esc(ho.name)},`);
    w(`  ${esc(ho.phone)},`);
    w(`  'homeowner'::user_role,`);
    w(`  0,`);
    w(`  true,`);
    w(`  'local',`);
    w(`  true,`);
    w(`  ${esc(ho.avatar_url)}`);
    w(`)`);
    w(`ON CONFLICT (email) DO UPDATE SET`);
    w(`  password_hash = EXCLUDED.password_hash,`);
    w(`  name = EXCLUDED.name,`);
    w(`  phone = EXCLUDED.phone,`);
    w(`  is_active = EXCLUDED.is_active,`);
    w(`  avatar_url = EXCLUDED.avatar_url,`);
    w(`  email_verified = EXCLUDED.email_verified,`);
    w(`  updated_at = NOW();`);

    w(``);
    w(`INSERT INTO accounts (name, url, owner_user_id)`);
    w(`SELECT ${esc(ho.name + " (Demo)")}, ${esc(slug)}, u.id`);
    w(`FROM users u WHERE u.email = ${esc(ho.email)}`);
    w(`ON CONFLICT (url) DO NOTHING;`);

    w(``);
    w(`INSERT INTO account_users (account_id, user_id, role) SELECT a.id, u.id, 'owner'::account_role FROM accounts a JOIN users u ON u.email = ${esc(ho.email)} WHERE a.url = ${esc(slug)} ON CONFLICT DO NOTHING;`);
    w(`INSERT INTO account_users (account_id, user_id, role) SELECT a.id, (SELECT id FROM users WHERE email = ${esc(agent.email)} LIMIT 1), 'member'::account_role FROM accounts a JOIN users u ON u.email = ${esc(ho.email)} WHERE a.url = ${esc(slug)} ON CONFLICT DO NOTHING;`);

    w(``);
    w(`INSERT INTO properties (`);
    w(`  property_uid, account_id, passport_id, property_name, main_photo, address, address_line_1, city, state, zip`);
    w(`)`);
    w(`SELECT`);
    w(`  ${esc(uid)},`);
    w(`  a.id,`);
    w(`  ${esc(passportId)},`);
    w(`  ${esc(addr.address_line_1)},`);
    w(`  ${esc(p.main_photo)},`);
    w(`  ${esc(addr.full)},`);
    w(`  ${esc(addr.address_line_1)},`);
    w(`  ${esc(addr.city)},`);
    w(`  ${esc(addr.state)},`);
    w(`  ${esc(addr.zip)}`);
    w(`FROM accounts a`);
    w(`WHERE a.url = ${esc(slug)}`);
    w(`ON CONFLICT (property_uid) DO NOTHING;`);

    w(``);
    w(`INSERT INTO property_users (property_id, user_id, role) SELECT p.id, u.id, 'owner'::property_role FROM properties p JOIN accounts a ON a.id = p.account_id JOIN users u ON u.email = ${esc(ho.email)} WHERE a.url = ${esc(slug)} ON CONFLICT DO NOTHING;`);
    w(`INSERT INTO property_users (property_id, user_id, role) SELECT p.id, (SELECT id FROM users WHERE email = ${esc(agent.email)} LIMIT 1), 'editor'::property_role FROM properties p JOIN accounts a ON a.id = p.account_id JOIN users u ON u.email = ${esc(ho.email)} WHERE a.url = ${esc(slug)} ON CONFLICT DO NOTHING;`);
  }

  w(``);
  w(`COMMIT;`);

  fs.writeFileSync(OUT, lines.join("\n") + "\n", "utf8");
  console.log(`Wrote ${OUT} (${props.length} properties)`);
})();
